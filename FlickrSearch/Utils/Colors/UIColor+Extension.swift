import Foundation
import UIKit

extension UIColor {
    
    class func flickrYellow() -> UIColor {
        return UIColor(red: 253/255, green: 204/255, blue: 1/255, alpha: 1)
    }
    
    class func flickrGray() -> UIColor {
        return UIColor(red: 169/255, green: 169/255, blue: 169/255, alpha: 1)
    }
}
